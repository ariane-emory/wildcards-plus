function removeAddedDate(inputString) {
  return inputString.replace(/added-\d{4}-\d{2}-\d{2}/, ''); // Matches and removes the pattern added-YYYY-MM-DD
}

function clean_name(name) {
  return name.toLowerCase()
    .replace(/\s+/g, '_')
    .replace(/\-/g, '_')
    .replace(/\'/g, '')
    .replaceAll('ō', 'o')
    .replaceAll('ü', 'u')
    .replaceAll('é', 'e')
    .replaceAll('ó', 'o')
    .replaceAll('ł', 'l')
    .replaceAll('ń', 'n')
    .replaceAll('é', 'e')
    .replaceAll('ô', 'o')
    .replaceAll('á', 'a')
    .replaceAll('í', 'i')
    .replaceAll('ç', 'c')
    .replaceAll('ù', 'u')
    .replaceAll('ä', 'a')
    .replaceAll('ō', 'o')
    .replaceAll('è', 'e')
    .replaceAll('å', 'a')
    .replace(/\./g, '');
}

